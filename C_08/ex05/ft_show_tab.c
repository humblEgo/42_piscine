#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nbr)
{
	long long nbr_ll;

	nbr_ll = (long long)nbr;
	if (nbr_ll < 0)
	{
		write(1, "-", 1);
		nbr_ll *= -1;
	}
	if (nbr >= 10)
		ft_putnbr((int)(nbr / 10));
	ft_putchar((int)((nbr % 10) + '0'));
}

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		write(1, "\n", 1);
		ft_putnbr(par[i].size);
		write(1, "\n", 1);
		ft_putstr(par[i].copy);
		write(1, "\n", 1);
		i++;
	}
}
