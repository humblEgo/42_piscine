int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	idx;

	idx = 0;
	if (n == 0)
		return (0);
	while (s1[idx] == s2[idx] && s1[idx] && s2[idx] && idx + 1 < n)
		idx++;
	return (s1[idx] - s2[idx]);
}
