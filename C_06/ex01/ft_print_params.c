#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

int		main(int argc, char **argv)
{
	int i;
	int j;

	i = 1;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j])
		{
			putchar(argv[i][j]);
			j++;
		}
		i++;
		putchar('\n');
	}
	return (0);
}
